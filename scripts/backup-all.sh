# 1. Резервное копирование базы данных
echo -e "${YELLOW}📦 Шаг 1/2: Резервное копирование базы данных${NC}"
echo ""
./scripts/backup-database.sh

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""

# 2. Резервное копирование файлов пользователей
echo -e "${YELLOW}📦 Шаг 2/2: Резервное копирование файлов пользователей${NC}"
echo ""
./scripts/backup-uploads.sh

echo ""
echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
echo ""
echo -e "${GREEN}✅ Полное резервное копирование завершено!${NC}"
echo ""
echo -e "${YELLOW}📁 Все резервные копии находятся в:${NC}"
echo "   database/backups/"
echo ""
echo -e "${YELLOW}💡 Для восстановления:${NC}"
echo "   - База данных: ./scripts/restore-database.sh"
echo "   - Файлы: tar -xzf database/backups/uploads_YYYYMMDD_HHMMSS.tar.gz"
echo ""
