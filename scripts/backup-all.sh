#!/bin/bash

# ============================================
# Полное резервное копирование (БД + файлы пользователей)
# ============================================

set -e  # Остановка при ошибке

# Цвета для вывода
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
